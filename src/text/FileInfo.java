package text;

public class FileInfo {
	String name;
	String date;
	String time;
	String size;
	String folder;
	
	FileInfo(String name, String date, String time, String size, String folder) {
		this.name = name;
		this.date = date;
		this.time = time;
		this.size = size;
		this.folder = folder;
	}
}